package com.example.pk836_6senses.Operator;

public class locationSave_Operator {
    String OP_LATS, OP_LONGS;

    public locationSave_Operator() {
    }
    public locationSave_Operator(String OP_LATS, String OP_LONGS) {
        this.OP_LATS = OP_LATS;
        this.OP_LONGS = OP_LONGS;
    }

    public String getOP_LATS() {
        return OP_LATS;
    }

    public void setOP_LATS(String OP_LATS) {
        this.OP_LATS = OP_LATS;
    }

    public String getOP_LONGS() {
        return OP_LONGS;
    }

    public void setOP_LONGS(String OP_LONGS) {
        this.OP_LONGS = OP_LONGS;
    }
}
